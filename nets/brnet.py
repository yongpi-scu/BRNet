    net = BRNet(num_classes=num_classes, drop_rate=drop_rate, color_channels=color_channels)
    if pretrained:
        exclusions = ['fc.weight', 'fc.bias', 'AuxLogits', 'Conv2d_1a_3x3']
        net = load_pretrain(net, 'BRNet', exclusions)
